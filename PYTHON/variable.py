#sep short for separator, using sep=':' would separate the arguments by a colon and sep='##' would separate the arguments by two pound signs.
print('The value of 3+4 is', 3+4,'.')
print('The value of 3+4 is ', 3+4, '.', sep='')

#end The print function will automatically advance to the next line.There is optional argument called end that you can use to keep the print function from advancing to the next line.
print('On the first line', end='')
print('On the second line')


